import { readdir, readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const schemasDir = resolve(root, 'schemas/domain');
const outputFile = resolve(root, 'packages/shared/src/generated/core-schema-types.ts');

const refTypeMap = new Map([
  ['./_defs/common.schema.json#/$defs/id', 'string'],
  ['./_defs/common.schema.json#/$defs/dateTime', 'string'],
  ['./_defs/common.schema.json#/$defs/date', 'string'],
  ['./_defs/common.schema.json#/$defs/email', 'string'],
  ['./_defs/common.schema.json#/$defs/nonNegativeNumber', 'number'],
  ['./audit-entry.schema.json', 'Record<string, unknown>'],
]);

function indent(lines, depth) {
  const prefix = '  '.repeat(depth);
  return lines
    .split('\n')
    .map((line) => (line.length > 0 ? `${prefix}${line}` : line))
    .join('\n');
}

function schemaToType(schema, depth = 0) {
  if (!schema) {
    return 'unknown';
  }

  if (schema.$ref) {
    return refTypeMap.get(schema.$ref) ?? 'Record<string, unknown>';
  }

  if (schema.enum) {
    return schema.enum.map((value) => JSON.stringify(value)).join(' | ');
  }

  if (Array.isArray(schema.type)) {
    return schema.type
      .map((typeValue) => {
        if (typeValue === 'null') {
          return 'null';
        }

        return schemaToType({ ...schema, type: typeValue }, depth);
      })
      .join(' | ');
  }

  if (schema.type === 'string') {
    return 'string';
  }

  if (schema.type === 'integer' || schema.type === 'number') {
    return 'number';
  }

  if (schema.type === 'boolean') {
    return 'boolean';
  }

  if (schema.type === 'array') {
    return `${schemaToType(schema.items, depth)}[]`;
  }

  if (schema.type === 'object') {
    const required = new Set(schema.required ?? []);
    const entries = Object.entries(schema.properties ?? {});

    if (entries.length === 0) {
      return schema.additionalProperties ? 'Record<string, unknown>' : '{}';
    }

    const lines = entries.map(([propertyName, propertySchema]) => {
      const optional = required.has(propertyName) ? '' : '?';
      return `${propertyName}${optional}: ${schemaToType(propertySchema, depth + 1)};`;
    });

    return ['{', indent(lines.join('\n'), 1), '}'].join('\n');
  }

  return 'unknown';
}

async function main() {
  const files = (await readdir(schemasDir))
    .filter((file) => file.startsWith('core-') && file.endsWith('.schema.json'))
    .sort();

  const declarations = [];

  for (const file of files) {
    const schema = JSON.parse(await readFile(resolve(schemasDir, file), 'utf8'));
    const title = schema.title;

    if (!title) {
      throw new Error(`Schema ${file} has no title.`);
    }

    declarations.push(`export type ${title} = ${schemaToType(schema)};`);
  }

  const generatedAt = new Date().toISOString();
  const content = [
    '// ⚠️ AUTO-GENERATED FILE — DO NOT HAND-EDIT.',
    '// Generated by scripts/generate-core-schema-types.mjs.',
    `// Generated at: ${generatedAt}`,
    '',
    ...declarations,
    '',
  ].join('\n');

  await writeFile(outputFile, content, 'utf8');
  console.log(`Generated ${outputFile}`);
}

main().catch((error) => {
  console.error('Failed to generate core schema types:', error);
  process.exit(1);
});
