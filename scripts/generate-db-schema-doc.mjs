import { readdir, readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const schemasDir = resolve(root, 'schemas/domain');
const outputFile = resolve(root, 'docs/generated/db-schema.md');

async function main() {
  const files = (await readdir(schemasDir)).filter((file) => file.endsWith('.schema.json')).sort();

  const rows = [];

  for (const file of files) {
    const fullPath = resolve(schemasDir, file);
    const schema = JSON.parse(await readFile(fullPath, 'utf8'));
    const title = schema.title ?? file.replace('.schema.json', '');
    const propertyCount = Object.keys(schema.properties ?? {}).length;

    rows.push(`| \`${title}\` | \`schemas/domain/${file}\` | ${propertyCount} |`);
  }

  const content = [
    '<!-- ⚠️ AUTO-GENERATED FILE — DO NOT HAND-EDIT ⚠️ -->',
    '<!-- This file is generated by `make generate` from the JSON Schemas in schemas/domain/. -->',
    '<!-- Any manual edits will be overwritten on the next generation run. -->',
    '',
    '# Database Schema (Generated)',
    '',
    '| Entity | Source Schema | Property Count |',
    '|---|---|---|',
    ...rows,
    '',
    '---',
    '',
    '## Generation Instructions',
    '',
    '```bash',
    'make generate',
    'make schemas',
    '```',
    '',
  ].join('\n');

  await writeFile(outputFile, content, 'utf8');
  console.log(`Generated ${outputFile}`);
}

main().catch((error) => {
  console.error('Failed to generate db schema docs:', error);
  process.exit(1);
});
